var counter = 0;

function btn_decrease() {
  counter--;
  document.getElementById("count").innerHTML = counter;
}

var counter = 0;

function btn_increase() {
  counter++;
  document.getElementById("count").innerHTML = counter;
}

var counter = 0;

function btn_reset() {
  counter = 0;
  document.getElementById("count").innerHTML = counter;
}
